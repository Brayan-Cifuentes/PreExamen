    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection(BD, Usuario, Clave);
            PreparedStatement pst = cn.prepareStatement("select * from Factura where ID_Factura = ?");
            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_iddetalle.setText(rs.getString("ID_Factura"));
                txt_idfactura.setText(rs.getString("ID_Cliente"));
                txt_idproducto.setText(rs.getString("Fecha"));
                txt_cantidad.setText(rs.getString("Total"));
                //txt_direccion.setText(rs.getString("Direccion"));

            } else {
                JOptionPane.showMessageDialog(null, "Factura no registrada.");
            }

        }catch (Exception e){

        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Codigo que permite insertar registros en al base de datos
        try{
            Connection cn = DriverManager.getConnection(BD, Usuario, Clave);
            PreparedStatement pst = cn.prepareStatement("insert into Detalle_Factura values(?,?,?,?,?,?)");

            pst.setString(1, txt_iddetalle.getText().trim());
            pst.setString(2, txt_idfactura.getText().trim());
            pst.setString(3, txt_idproducto.getText().trim());
            pst.setString(4, txt_cantidad.getText().trim());
            pst.setString(5, txt_precio.getText().trim());
            pst.setString(6, txt_subtotal.getText().trim());
            

            pst.executeUpdate();

            txt_iddetalle.setText("");
            txt_idfactura.setText("");
            txt_idproducto.setText("");
            txt_cantidad.setText("");
            txt_precio.setText("");
            txt_subtotal.setText("");
           
            lbl_estatus.setText("Registro exitoso.");
        }catch (Exception e){

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite modificar registros en la base de datos
        try {
            String ID = txt_buscar.getText().trim();

            Connection cn = DriverManager.getConnection(BD, Usuario, Clave);
            PreparedStatement pst = cn.prepareStatement("update Detalle_Factura set ID_Factura = ?, ID_Cliente=?, Fecha=?, Total=? where ID_Factura = " + ID);
            
            pst.setString(1, txt_iddetalle.getText().trim());
            pst.setString(2, txt_idfactura.getText().trim());
            pst.setString(3, txt_idproducto.getText().trim());
            pst.setString(4, txt_cantidad.getText().trim());
           
            pst.executeUpdate();
            
            txt_iddetalle.setText("");
            txt_idfactura.setText("");
            txt_idproducto.setText("");
            txt_cantidad.setText("");
            
            
            lbl_estatus.setText("Modificaci√≥n Exitosa.");

        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton2ActionPerformed

